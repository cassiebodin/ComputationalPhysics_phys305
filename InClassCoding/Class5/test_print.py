x = 3.14159265359

print (x)

print ('{0:.2f}'.format(x))

print ('{:.3f}'.format(x))

print ('{:8.3f}'.format(x))

print ('{:.3e}'.format(x))

y = 2.71828

print('{1} and {0}'.format(x, y))

print('{0:.2e} and {1:.4e}'.format(x, y))

i = 31

print (i)

print ('{:6d}'.format(i))

print ('{:06d}'.format(i))

print ('{:b}'.format(i))

print ('{:x}'.format(i))
